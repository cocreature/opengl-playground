void Scene::init() {
    textures = packed_freelist<Texture>(512);
    meshes = packed_freelist<Mesh>(512);
    transforms = packed_freelist<Transform>(4096);
    instances = packed_freelist<Instance>(4096);

    camera.pos = {0.0f, 0.0f, 3.0f};
    camera.target = {0.0f, 0.0f, -1.0f};
    camera.up = {0.0f, 1.0f, 0.0f};

    // =================
    // initialize meshes
    // =================
    GLuint vertexArray;
    GLuint vertexBuffer;
    glGenVertexArrays(1, &vertexArray);
    glGenBuffers(1, &vertexBuffer);

    glBindVertexArray(vertexArray);

    glBindBuffer(GL_ARRAY_BUFFER, vertexBuffer);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    // position attribute
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(GLfloat),
                          (GLvoid *)0);
    glEnableVertexAttribArray(0);

    // texcoord attribute
    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(GLfloat),
                          (GLvoid *)(3 * sizeof(GLfloat)));
    glEnableVertexAttribArray(2);

    glBindVertexArray(0);

    // =================
    // container texture
    // =================
    GLuint containerTexture;
    glGenTextures(1, &containerTexture);
    glBindTexture(GL_TEXTURE_2D, containerTexture);

    // texture wrapping params
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_REPEAT);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_REPEAT);

    // texture filtering params
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

    SDL_Surface *surface = IMG_Load("../textures/container.jpg");

    glTexImage2D(GL_TEXTURE_2D, 0, GL_RGB, surface->w, surface->h, 0, GL_RGB,
                 GL_UNSIGNED_BYTE, surface->pixels);
    glGenerateMipmap(GL_TEXTURE_2D);

    SDL_FreeSurface(surface);
    glBindTexture(GL_TEXTURE_2D, 0);

    // ==========
    // Fill scene
    // ==========
    uint32_t textureID = textures.insert({containerTexture});
    uint32_t meshID =
        meshes.insert({vertexArray, vertexBuffer, sizeof(vertices), textureID});

    for (GLuint i = 0; i < 10; i++) {
        GLfloat angle = glm::radians(20.0f * i);
        glm::quat rot = angleAxis(angle, normalize(glm::vec3(1.0, 0.3, 0.5f)));
        uint32_t transformID = transforms.insert({cubePositions[i], rot});
        instances.insert({meshID, transformID});
    }
}
